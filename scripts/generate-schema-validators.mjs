import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

const repositoryRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const checkOnly = process.argv.includes("--check");
const generatedHeader = `// Generated by scripts/generate-schema-validators.mjs. Do not edit.\n// @ts-nocheck\n\n`;
const runtimeModules = [
  {
    request: "ajv/dist/runtime/ucs2length",
    importPath: "ajv/dist/runtime/ucs2length.js",
    moduleName: "ucs2lengthModule",
    valueName: "ucs2lengthRuntime"
  },
  {
    request: "ajv/dist/runtime/equal",
    importPath: "ajv/dist/runtime/equal.js",
    moduleName: "equalModule",
    valueName: "equalRuntime"
  }
];

const targets = [
  {
    output: "packages/project-format/src/generated/schema-validators.ts",
    options: {
      allErrors: true,
      strict: true,
      removeAdditional: false,
      useDefaults: false,
      coerceTypes: false
    },
    schemas: {
      projectValidator: "packages/project-format/src/schemas/project-v1.schema.json",
      lockfileValidator: "packages/project-format/src/schemas/lockfile-v1.schema.json",
      documentEnvelopeValidator:
        "packages/project-format/src/schemas/document-envelope-v1.schema.json",
      historyValidator: "packages/project-format/src/schemas/history-v1.schema.json"
    }
  },
  {
    output:
      "extensions/standard-motion-document/src/generated/composition-validator.ts",
    options: { allErrors: true, strict: true },
    schemas: {
      compositionValidator:
        "extensions/standard-motion-document/src/schemas/composition-v2.schema.json"
    }
  }
];

async function loadJson(relativePath) {
  const source = await readFile(path.join(repositoryRoot, relativePath), "utf8");
  return JSON.parse(source);
}

async function generate(target) {
  const ajv = new Ajv2020({
    ...target.options,
    code: { source: true, esm: true, lines: true }
  });
  const exportsById = {};
  for (const [exportName, schemaPath] of Object.entries(target.schemas)) {
    const schema = await loadJson(schemaPath);
    if (typeof schema.$id !== "string" || schema.$id.length === 0) {
      throw new Error(`${schemaPath} must define a non-empty $id`);
    }
    ajv.addSchema(schema);
    exportsById[exportName] = schema.$id;
  }
  let generated = standaloneCode(ajv, exportsById);
  let runtimePrelude = "";
  for (const runtime of runtimeModules) {
    const expression = `require("${runtime.request}").default`;
    if (!generated.includes(expression)) continue;
    generated = generated.replaceAll(expression, runtime.valueName);
    runtimePrelude +=
      `import ${runtime.moduleName} from "${runtime.importPath}";\n` +
      `const ${runtime.valueName} = ${runtime.moduleName}.default ?? ${runtime.moduleName};\n`;
  }
  return `${generatedHeader}${runtimePrelude}\n${generated.trimEnd()}\n`;
}

const stale = [];
for (const target of targets) {
  const outputPath = path.join(repositoryRoot, target.output);
  const generated = await generate(target);
  if (checkOnly) {
    const existing = await readFile(outputPath, "utf8").catch(() => undefined);
    if (existing !== generated) stale.push(target.output);
  } else {
    await mkdir(path.dirname(outputPath), { recursive: true });
    await writeFile(outputPath, generated, "utf8");
  }
}

if (stale.length > 0) {
  throw new Error(
    `Generated schema validators are stale:\n${stale.map((value) => `- ${value}`).join("\n")}\nRun pnpm generate:schema-validators.`
  );
}
